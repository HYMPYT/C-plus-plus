#include <iostream>
using namespace std;

// приклад зациклення (завжди істинна УПЦ)
void main()
{
	float i = 10;
	while (i > 1) 
	{
		cout << " " << rand() % 1000;
		i++;
	}
	cout << endl;
	/* i > 1 умова продовження циклу (УПЦ)
	оскільки "і" приймає значення 10 і надалі лише зростає (i++),
	то УПЦ буде істинною завжди - відбувається зациклення */
}